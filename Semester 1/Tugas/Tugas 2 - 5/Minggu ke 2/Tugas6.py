# Menghitung luas segitiga

alas = 5
tinggi = float(10.5)
luas = 0.5 * alas * tinggi
print("alas :",alas)
print('tinggi :', tinggi)
print('luas segitiga adalah :','1/2 *', alas, '*', tinggi, '=', luas)
print('tipe data dari alas :', type(alas))
print('tipe data dari tinggi :', type(tinggi))
print('tipe data dari luas :', type(luas))
